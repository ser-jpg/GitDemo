package newProje;
/*
Prints number from 100-0 with 1/2 steps using do while loop
sample 100
        99.5
        99
 */
public class MathProblem {
    public static void main(String[]args){
           int start1=100;
           double start2=99.5;
        do{
            System.out.println(start1);
            start1--;
            System.out.println(start2);
            start2--;




















        }while(start1>0);







    }
}
