package newProje;
/*
Print all dates for 2019 ( assume all months 30 are days)
sample output:
1/1/2019
2/1/2019
10/1/2019

 */
public class Calander {
    public static void main(String[]args){
        for(int month=1;month<=12;month++){




            for(int day=1;day<=30;day++){

                    System.out.println(month+"/"+day+"/"+"2019");






            }
        }


    }
}
